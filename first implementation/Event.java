public class Event {
	
	private int EventSeqNum;
	
	private int EventDataValue;
	
	
	public int getEventSeqNum() {
		return EventSeqNum;		
	}
	
	
	public int getEventDataValue() {
		return EventDataValue;		
	}


	public void setEventSeqNum(int eventSeqNum) {
		EventSeqNum = eventSeqNum;
	}


	public void setEventDataValue(int eventDataValue) {
		EventDataValue = eventDataValue;
	}

}
